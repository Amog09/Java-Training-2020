package tasktwo.assignment;

public class SwapVariables {

	public static void main(String[] args) {
		int min = 99;
		int max = 1000;
		int result;
		result = min;
		min = max;
		max =  result;
			
		
		System.out.println("Min Number : "+ min + "\nMax Number :  " +max);
		

	}

}
